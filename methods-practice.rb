def timesOneThousand (number)
    number*1000.0
end
puts timesOneThousand (8)

def howOldWillBeIn2030(year)
    15 + year
end
puts howOldWillBeIn2030(13)

def makeExcited(word)
    "#{word}".upcase + "!!!"
end
puts makeExcited("hello")

def happyBackwards (word)
    "#{word}".reverse + ":)"
end
puts happyBackwards("cool")